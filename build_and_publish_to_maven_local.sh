#!/bin/bash

SECONDS=0

####################################################################################################
###########################################  Clean  ################################################
####################################################################################################

./gradlew clean

####################################################################################################
##########################################  Build  #################################################
####################################################################################################

./gradlew :libraries:storage-core:build
./gradlew :libraries:storage-core:storage-core-test:build


####################################################################################################
####################################  Publish to maven local  ######################################
####################################################################################################

./gradlew :libraries:storage-core:publishToMavenLocal
./gradlew :libraries:storage-core:storage-core-test:publishToMavenLocal


echo "total time taken $SECONDS sec"